#  WITHOUT SCIKIT-LEARN (Manual Gradient Descent)
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import time

# Load dataset
df = pd.read_csv('/content/drive/MyDrive/Ml project/diamonds.csv')
df.drop(["Unnamed: 0"], axis=1, inplace=True)

# Clean and use only numeric features
df = df[(df["x"] > 0) & (df["y"] > 0) & (df["z"] > 0)]
X = df[['carat', 'depth', 'table', 'x', 'y', 'z']]
y = df['price'].values.reshape(-1, 1)

# Normalize features manually
X = (X - X.mean()) / X.std()

# Split
from sklearn.model_selection import train_test_split
X_train, X_test, y_train, y_test = train_test_split(X.values, y, test_size=0.2, random_state=42)

# Add bias term manually (theta_0)
X_train_b = np.c_[np.ones((X_train.shape[0], 1)), X_train]
X_test_b = np.c_[np.ones((X_test.shape[0], 1)), X_test]

# Initialize theta
theta = np.zeros((X_train_b.shape[1], 1))
alpha = 0.01
iterations = 1000
m = len(y_train)
costs = []

# Start timer
start_time = time.time()

# Gradient Descent
for i in range(iterations):
    h = np.dot(X_train_b, theta)
    error = h - y_train
    cost = (1/(2*m)) * np.sum(np.square(error))
    costs.append(cost)

    gradients = (1/m) * np.dot(X_train_b.T, error)
    theta = theta - alpha * gradients

end_time = time.time()

# Predict and evaluate
y_pred_manual = np.dot(X_test_b, theta)

from sklearn.metrics import mean_squared_error, r2_score
rmse_manual = np.sqrt(mean_squared_error(y_test, y_pred_manual))
r2_manual = r2_score(y_test, y_pred_manual)

print(f"Manual RMSE: {rmse_manual:.2f}")
print(f"Manual R2 Score: {r2_manual:.4f}")
print(f"Manual Time Taken: {end_time - start_time:.4f} seconds")

# Plot cost vs iteration
plt.figure(figsize=(6, 6))
plt.plot(costs)
plt.title("Gradient Descent: Cost vs Iteration")
plt.xlabel("Iteration")
plt.ylabel("Cost")
plt.grid()
plt.tight_layout()
plt.show()

#Actual vs Predicted Plot
plt.figure()
plt.figure(figsize=(6, 6))
plt.scatter(y_test, y_pred_manual, alpha=0.5, color='blue', label='Manual Predictions')
plt.plot([y_test.min(), y_test.max()], [y_test.min(), y_test.max()], 'r--', label='Perfect Fit')
plt.xlabel("Actual Price")
plt.ylabel("Predicted Price")
plt.title("Actual vs Predicted (Manual)")
plt.legend()
plt.grid()
plt.tight_layout()
plt.show()

#Residual Plot
plt.figure()
plt.figure(figsize=(6, 6))
residuals_manual = y_test - y_pred_manual
sns.residplot(x=y_pred_manual.flatten(), y=residuals_manual.flatten(), lowess=True, color='darkred')
plt.title("Residual Plot (Manual)")
plt.xlabel("Predicted")
plt.ylabel("Residuals")
plt.grid()
