import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import time

# Load dataset
df = pd.read_csv('/content/drive/MyDrive/Ml project/diamonds.csv')
df.drop(["Unnamed: 0"], axis=1, inplace=True)

# Clean numeric features
df = df[(df["x"] > 0) & (df["y"] > 0) & (df["z"] > 0)]
X = df[['carat', 'depth', 'table', 'x', 'y', 'z']]
y = df['price'].values.reshape(-1, 1)

# Normalize
from sklearn.preprocessing import StandardScaler
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# Split
from sklearn.model_selection import train_test_split
X_train, X_test, y_train, y_test = train_test_split(X_scaled, y, test_size=0.2, random_state=42)

# Train with sklearn
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, r2_score

start_time = time.time()

model = LinearRegression()
model.fit(X_train, y_train)
y_pred_sklearn = model.predict(X_test)

end_time = time.time()

# Evaluate
rmse_sk = np.sqrt(mean_squared_error(y_test, y_pred_sklearn))
r2_sk = r2_score(y_test, y_pred_sklearn)

print(f"Scikit-learn RMSE: {rmse_sk:.2f}")
print(f"Scikit-learn R2 Score: {r2_sk:.4f}")
